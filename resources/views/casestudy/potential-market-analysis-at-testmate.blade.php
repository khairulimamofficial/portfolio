                        <p>TAM itself refers to the Total Addressable Market which describes the total market demand for a product or service as a whole. In addition, TAM can also be used to find out customer segments that are still untapped.
                        </p>
                        <p>Then, SAM stands for Serviceable Addressable Market which measures the business's ability to provide services. SAM is used to calculate the business's ability to reach the market objectively. SAM can also be used to help businesses determine their niche and medium-term goals. The reason is, startups as new companies, certainly have limitations in providing services. Therefore, a realistic calculation is needed regarding the market size that is the actual target market.
                        </p>
                        <p>SOM stands for Serviceable Obtainable Market. SOM refers to the total market that is already using the company's products or services. Calculating SOM can help businesses to know the worst-case scenario, which is only being able to achieve a small portion of TAM. Calculating SOM is important to demonstrate the business's ability to determine and provide services to the target market.
                        </p>
