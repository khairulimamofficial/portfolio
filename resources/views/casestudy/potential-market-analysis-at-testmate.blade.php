                        <h6>Introduction</h6>
                        <p>TAM SAM SOM is a calculation method to measure the market potential of a business. Usually, this method is used by entrepreneurs who will build a new business or startup. With the TAM SAM SOM method, entrepreneurs are able to:
                        </p>
                        <ol class="work-description-desc-list-number">
                            <li>Forecast the growth of the business</li>
