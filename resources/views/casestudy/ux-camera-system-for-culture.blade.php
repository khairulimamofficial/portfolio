                        <h4>Introduction</h4>
                        <p>Millennials are less familiar with and proud of these traditional clothes. This will have an impact on the loss of traditional clothing culture that has existed since long ago and then replaced by outside culture. Based on data from the Central Statistics Agency, the percentage of households using traditional clothing was only around 18.96% during the last three months of 2018.</p>
                        <p>Local culture is fading due to the lack of the next generation who have an interest in learning and inheriting it. According to Malinowski, a higher and active culture will influence a lower and passive culture through cultural contact (Malinowski in Mulyana, 2005: 21). </p>
                        <p>Article 32 Paragraph 1 of the 1945 Constitution states "The state promotes Indonesian national culture in the midst of world civilization by guaranteeing the freedom of the community to maintain and develop its cultural values." On the basis of this statement, the state must play an active role in carrying out the agenda of promoting national culture, one of which is in traditional clothing.</p>
                        <p>Nowadays, to introduce these things to the public, a unique way and direct contact with the users is needed. Therefore, we created a solution called Maya (Mata Budaya). Maya is a solution application to ground traditional clothing online containing features, namely the creation of stickers made with Augmented Reality technology.</p>
                    </div>
                    <div>
                        <h6>Objectives</h6>
                        <ol class="work-description-desc-list-number">
                            <li>Increase the love for Indonesian traditional clothing culture.</li>
                            <li>Traditional clothing will be worn more often by the community.</li>
                            <li>People use application features such as stickers that are more useful
                                than other stickers</li>
                           
                        </ol>
                    </div>
                    <div>
                        <h6>Benefits</h6>
                        <ol class="work-description-desc-list-number">
                            <li>People become proud to wear Indonesian traditional clothes.</li>
                            <li>Viral culture of traditional clothing in Indonesia so that the use of traditional clothing is 
                                increased in the wider community.</li>
                            <li>Expressing through social media chats with a cultural display of traditional clothing.
                                culture.</li>
                        </ol>
                    </div>

