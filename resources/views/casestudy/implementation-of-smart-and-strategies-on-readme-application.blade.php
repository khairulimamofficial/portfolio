                    <div>
                        <h4>A. Introduction</h4>
                        <p>Books are the windows to the world. With books, one can explore the outside world without having to travel to the outside world. One will gain unlimited knowledge, communicate around the clock, and meet people from all over the world. </p>
                        <p>Visually impaired refers to a term for people who have difficulty in using vision or cannot function their sense of sight from mild vision to total blindness. However, the number of blind people is not followed by easy access to education. If this situation is allowed to continue, it will cause blind people to not have good reasoning power and reduce their thinking power.
                            </p>
                        <p>In accordance with Law No. 4 of 1997 concerning Persons with Disabilities, states that: "Every educational institution provides equal opportunities and treatment to persons with disabilities as learners in units, pathways, types, and levels of education in accordance with the type and degree of disability." Therefore, it can be concluded that people with disabilities have the same rights in the utilization of technology, information and communication. The potential that will be gained in increasing interest in reading is to help people with disabilities to become knowledgeable and independent individuals, at least for themselves. If they have knowledge, it will help in creating outstanding human resources.
                            </p>
                    
                    </div>
                    <div>
                        <h4>B. Goal</h4>
                        <p>Through the results of this product, it is hoped that blind people can get access to reading and improve literacy independently, so that blind people can expand their knowledge and insight into the latest available on the internet. In addition, for the government as an effort to achieve sustainable development goals point 4 regarding quality education (Quality education) Ensure quality education that is appropriate and inclusive and encourage lifelong learning opportunities for all people. 
                            </p>
                    </div>
                    <div>
                        <h4>C. Solution</h4>
                        <p>The solution in integrating all aspects needed by the visually impaired is built in an application called ReadMe. ReadMe is a smartphone application aimed at the visually impaired to improve literacy independently. 
                        </p>
                        <p>ReadMe maximizes the use of audio to assist the visually impaired in finding reading materials, reading materials, and running all activities in the application by listening. ReadMe is an alternative to Braille format books that tend to be limited and require special costs to make, so ReadMe will be an effective and efficient solution in meeting the literacy needs of the blind.
                        </p>
                    </div>

                    <div>
                        <h4>SMART concept on ReadMe</h4>
                        <img class="work-detail-img" src="img/work/readme/smart.webp" alt="uat-roles">
                        <p>The SMART concept is used as the first step to provide an initial description of problem solving and targets to be achieved in the idea of ReadMe. The following are the points of explanation.
                        </p>
                        <ol class="work-description-desc-list-number">
                            <li><b>Specific</b><br>
                                Readme application focuses on blind people in Indonesia. Many blind people are gathered in SLB-A spread across various cities in Indonesia. Based on SUPAS data from BPS in 2015, the number of 3 categories of blind people in Indonesia has 6.36% of blind people from the total population of Indonesia.
                            </li>
                            <li><b>Measurable</b><br>
                                ReadMe application targets SLB-A spread in Medan city, as the place where ReadMe application is established and can be measured user literacy assessment with the stages of testing a person's literacy level. Then, the application will continue to grow through publications and communities spread across various regions.
                            </li>
                            <li><b>Acceptable</b><br>
                                ReadMe application is very useful to improve the literacy of the blind with its ease and generosity. Of course, it will pay attention to the norms or rules when accessing a given site and blocking on prohibited sites.
                            </li>
                            <li><b>Realistic</b><br>
                                This application can be built and implemented because of the technology that is already available and can be developed. Device requirements are also affordable and easy to obtain.
                            </li>
                            <li><b>Timebound</b><br>
                                The target of achieving the success of this application will be tested for the next 1 year. In that one year, surveys will continue to be conducted to several blind people who have used the application and are in North Sumatra. A campaign will be conducted about the importance of an application to support the improvement of blind literacy with easy access to information and useful reading.
                            </li>
                            
                        </ol>
                    </div>
                


                    <div>
                        <h4>Implementation Strategy for ReadMe</h4>
                        <p>The ReadMe application deployment implementation strategy is intended to make the application acceptable and usable by blind people in Indonesia. There are 3 strategies that will be carried out as follows.
                        </p>
                        <ol class="work-description-desc-list-number">
                            <li><b>Formal Education of the Visually Impaired</b><br>
                                The use is prioritized in the formal education process that must be taken through Sekolah Luar Biasa (SLB). SLB part A is intended for visually impaired children. Approach the application by visiting SLB extensively and providing briefings to utilize ReadMe application in the teaching-learning process.
                            </li>
                            <li><b>Approach through the Blind and Disabled Community</b><br>
                                Community is a platform for dissemination of information for people with disabilities, especially blind people. Many blind people in Indonesia are currently registered with the Indonesian Blind Association (Pertuni). Through approaches and socialization accompanied by cooperation with the community, it is hoped that it can reach blind people who come from various backgrounds. The community approach also accelerates the development of ReadMe program and application information through word of mouth marketing.
                            </li>
                            <li><b>Multi-Channel E-Campaign</b><br>
                                Online publications are campaigned through various channels, namely (1) ReadMe's official web portal, (2) electronic media, such as electronic newspapers, and (3) intensifying the e-campaign of ReadMe programs and applications on social media such as Twitter, Facebook, and Instagram. Publications are also carried out through cooperation with social care communities and communities of people with disabilities on social media, the approach through digital communities is easier to do because it can disseminate information to the the target audience.
                            </li>

                            
                        </ol>
