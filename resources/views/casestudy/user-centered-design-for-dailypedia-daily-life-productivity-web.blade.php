                    <div>
                        <h6>Platform yang Digunakan</h6>
                        <p>Situs web Dailypedia dapat dibuka dalam perangkat manapun seperti browser desktop, browser smartphone, dan browser tablet selama browser tersebut mendukung HTML5 seperti Chrome, Firefox, dan Edge.
                            </p>
                    </div>

                    <div>
                        <h6>Batasan Aplikasi</h6>
                        <p>Karena aplikasi yang kami buat berbasis web, maka situs web Dailypedia ini memiliki batasan-batasan sebagai berikut:</p>
                        <ol class="work-description-desc-list-number">
                            <li>Membutuhkan koneksi internet untuk mengakses web, serta notifikasi</li>
                            <li>Membutuhkan cookies untuk menyimpan data dalam web</li>
                            <li>Direkomendasikan menggunakan browser seperti Google Chrome, firefox, dan Edge.</li>
                        </ol>
                    </div>

                    <div>
                        <h6>Analisis Desain</h6>
                        <p>Kami membuat situs web berdasarkan analisis yang kami dapatkan selama masa Praproduksi. Analisis kami mencakup: Permasalahan utama yang muncul, batasan sistem dan perangkat, dan hasil evaluasi kami terhadap pandangan pengguna dan ide utama kami.
                        </p>
                        <p>Hal ini kami lakukan dengan tujuan mencapai User Experience terbaik untuk pengguna, tentunya tanpa mengorbankan User Interface yang ada, kami juga berusaha untuk lebih mengefisienkan sekaligus mengefektifkan pengaplikasian situs web kami sehingga kami dapat memanjakan mata pengguna serta memberikan efek psikologis agar mood pengguna dapat kembali baik.
                        </p>
                        <p>Oleh karena itu, kami memberikan pengguna kebebasan untuk menentukan tema situs web sesuai mood-nya hari itu di setiap kali pengguna mengunjungi situs web kami dengan menggunakan warna-warna khusus, seperti:
                        </p>
                        <ul class="work-description-desc-list">
                            <li>Untuk suasana hati netral (neutral), warna dasar yang dipakai adalah biru muda karena biru diyakini dapat memberikan rasa kedamaian, intelektual, dan juga dapat membangkitkan produktivitas.</li>
                            <li>Untuk suasana hati yang senang (happy), warna dasar yang dipakai adalah kuning karena warna tersebut menggambarkan kegembiraan dan juga dapat membangkitkan semangat.</li>
                            <li>Untuk suasana hati yang sedang jatuh cinta (love), warna dasar yang dipakai adalah merah muda karena warna tersebut identik dengan cinta sehingga dapat memberikan kesan lembut.</li>
                            <li>Untuk suasana yang sedang murung (moody), warna dasar yang dipakai adalah hijau karena warna hijau dapat menenangkan mata dan juga memberikan kenyamanan bagi yang melihat.</li>
                            <li>Untuk suasana hati yang sedang sedih, warna dasar yang dipakai adalah gabungan warna biru muda dan merah muda agar didapat warna yang halus untuk dilihat.</li>
                        </ul>
                    </div>

