                        <h6>Skenario Penggunaan</h6>
                        <p>Berikut skenario penggunaan situs web DailyPedia:
                            </p>
                        <img class="work-detail-img" src="img/work/dailypedia/dailypedia-6.webp" alt="test-case">
                        <p>Sabrina Benita adalah seorang mahasiswi di salah satu perguruan tinggi di Indonesia. Ia terkadang memiliki banyak waktu senggang saat kuliah daring selama pandemi, namun masih terkendala pada suasana hati yang terkadang baik dan buruk. Selain itu, ia harus melawan kemalasannya dalam beraktivitas.
                        </p>
                        <p>Sabrina berkeinginan untuk melakukan banyak hal produktif, akan tetapi ia bingung untuk memulai kegiatan produktifnya, sehingga dia mulai menggunakan situs web DailyPedia sebagai solusinya.
                        </p>
                        <p>Untuk memulai, Sabrina direkomendasikan untuk menentukan suasana hatinya saat mengakses situs DailyPedia. Pilihan dari suasana hati yang dipilih akan menentukan tema warna dari situs DailyPedia, warna yang kemudian menjadi tema utama diharapkan dapat memberi semangat kepada Sabrina ke arah yang lebih positif.
                        </p>
                        <p>Kemudian, Sabrina diharuskan untuk melakukan pendaftaran melalui tombol Match Now untuk menyesuaikan minat/ketertarikan terhadap genre buku, video, maupun kerajinan yang ada. Pendaftaran ini bertujuan mendapatkan data Sabrina yang akan digunakan pada fitur reminder, berita terbaru, dan juga daftar rekomendasi yang akan muncul jika Sabrina mengaktifkan cookies pada situs web.
                        </p>
                        <p>Pada halaman Home, Sabrina akan mendapatkan informasi umum mengenai DailyPedia. Informasi tersebut berupa pengertian, manfaat, dan instruksi singkat dari fitur web.
                        </p>
                        <p>Sabrina dapat memulai kegiatan produktifnya saat mengakses Library. Pada bagian tersebut, ia bebas untuk mengakses konten manapun yang sedang dibutuhkan untuk mengisi waktu luangnya. Saat Sabrina menekan salah satu konten, maka ia akan diarahkan ke laman detail dari konten tersebut. Pada laman detail video dan buku, terdapat detail dari konten tersebut seperti author, synopsis, review pengguna lainnya, dan juga Sabrina dapat langsung menikmati video maupun buku secara langsung di dalam laman tersebut. Selain itu, terdapat juga laman kerajinan yang berisi langkah-langkah yang lengkap dan menarik untuk diikuti.
                        </p>
                        <p>Halaman Schedule berisi kegiatan terakhir Sabrina di situs DailyPedia yang dapat dilanjutkan kembali. Kemudian, terdapat laman profile berisi pengaturan akun dan juga daftar kegiatan yang telah diselesaikan.
                        </p>
