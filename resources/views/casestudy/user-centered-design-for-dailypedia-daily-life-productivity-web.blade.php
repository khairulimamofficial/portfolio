                    <div>
                        <h6>Introduction</h6>
                        <p>Menjadi pribadi yang produktif memiliki banyak kelebihan, di antaranya dapat menjaga pikiran selalu aktif, pikiran yang selalu aktif dapat menguatkan sel-sel otak yang dapat mengembangkan kemampuan otak. Otak yang selalu berkembang mempermudah seorang pribadi untuk berkembang menjadi pribadi yang lebih baik lagi. Dengan menjadi pribadi yang lebih baik, seseorang menjadi lebih mudah untuk menjaga kestabilan suasana hatinya, karena suasana hati dan produktivitas sangat berkaitan erat. Suasana hati yang bagus dapat mengarah pada produktivitas, produktivitas yang baik dapat mencegah prokrastinasi, yang nantinya juga dapat mencegah adanya penyakit mental seperti kecemasan berlebih (anxiety disorder) dan juga depresi.
                        </p>
                        <p>Sejumlah 74% dari 23 responden kami (16 tahun ke atas) menyatakan masih merasa kurang produktif selama pandemi Covid-19. Mereka menghabiskan waktunya di rumah dengan rebahan, bermain gim, menonton film, dan scrolling media sosial. Oleh karena kami membuat situs web bernama DailyPedia. DailyPedia adalah sebuah situs web berisi kumpulan rekomendasi film, buku, dan video dan dapat langsung dinikmati secara gratis dengan disesuaikan berdasarkan suasana hati dan ketertarikan setiap penggunanya sehingga dapat menggerakkan mereka untuk bisa lebih produktif.
                        </p>
                    </div>

                    <div>
                        <h6>Tujuan</h6>
                        <p>Manfaat yang akan didapat dari web kami ialah:</p>
                        <ol class="work-description-desc-list-number">
                            <li>Menginspirasi para anak muda yang mengakses web kami</li>
                            <li>Membangkitkan semangat untuk kembali produktif</li>
                            <li>Menjadi suatu sarana produktif untuk menghibur diri saat sedang malas</li>
                        </ol>
                    </div>

                    <div>
                        <h6>Keunggulan</h6>
                        <p>Kelebihan dan keunikan dari situs web “DailyPedia” adalah sebagai berikut.</p>
                        <ol class="work-description-desc-list-number">
                            <li>Situs memiliki tema yang dapat diatur berdasarkan suasana hati pengguna perharinya.</li>
                            <li>Rekomendasi yang diatur berdasarkan gabungan suasana hati dan juga interest dari pengguna.</li>
                            <li>Memiliki fitur reminder untuk melanjutkan aktivitas terakhir dari pengguna melalui surel.</li>
                            <li>Memanfaatkan cookies yang jika diaktifkan akan mendapatkan informasi lebih dalam dan personal pada masing-masing pengguna.</li>
                            <li>Pengguna dapat menikmati konten-konten yang menghibur sekaligus dapat menambah ilmu sehingga menjadi produktif.</li>
                        </ol>
                    </div>

                    <div>
                        <h6>Metodologi Pengembangan</h6>
                        <p>Kami menerapkan Metode User Centered Design (UCD) dan Prototyping dalam menyiapkan dan merencanakan situs web dalam proses praproduksi kali ini. Metode ini terbentuk menjadi berikut:
                            </p>
                        <img class="work-detail-img img-small" src="img/work/dailypedia/dailypedia.webp" alt="test-case">
