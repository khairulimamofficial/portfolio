                    <div>
                        <h6>Target Pengguna</h6>
                        <p>Sasaran target pengguna situs web kami adalah pemuda/pemudi (terutama Gen-Z dan Gen Millenial) yang memiliki waktu luang atau sedang merasa stress (burnout syndrome) dengan pekerjaannya namun ingin merasa produktif kembali dengan cara yang lebih menyenangkan (fun).
                            </p>
                        <img class="work-detail-img" src="img/work/dailypedia/dailypedia-5.webp" alt="test-case">
