                    <div>
                        <h4>Introduction</h4>
                    </div>
                    <div>
                        <p>The fourth goal of the Sustainable Development Goals (SDGs) emphasizes the importance of providing inclusive and fair access to high-quality education for everyone, including individuals with visual impairments. In general, a blind person refers to the lack of vision or impaired vision. According to the 2015 Intercensal Population Surveys (SUPAS) from the Central Bureau of Statistics (BPS), it was reported that 0.13 percent of the population aged ten years and older experienced total blindness, 0.72 percent had severe visual impairment, and 5.51 percent had moderate visual impairment.</p>
                    </div>

                    <div>
                        <h4>Challenge</h4>
                    </div>
                    <div>
                        <p>Visually impaired individuals employ specialized learning techniques and tools to acquire knowledge, given their restricted vision. The difficulty arises from the limited accessibility to literacy for the blind, primarily due to the high production costs of braille books, which serve as the main source of information, and the challenges blind individuals face in reading braille. Another alternative literacy material can be accessed by blind individuals through audio media like audiobooks. However, the current availability of audiobooks falls short in comparison to the diverse range of literacy materials accessible today. Failure to address this issue promptly could result in impaired logical thinking and diminished cognitive abilities among blind individuals due to a lack of sufficient literacy resources.</p>
                    </div>

                    <div>
                        <h4>Solution</h4>
                    </div>
                    <div>
                        <p>Utilizing auditory media is an effective approach for individuals with visual impairment, allowing them to leverage their heightened sense of hearing. Research has shown that blind individuals exhibit increased sensitivity to auditory stimuli, enabling them to identify familiar voices and process voice-related information adeptly. Therefore, the implementation of a literacy support application based on voice can be a valuable solution for enhancing the literacy skills of blind individuals.</p>
                    </div>

                    <div>
                        <h4>Competitor</h4>
                    </div>
                    <div>
                        <ul class="work-description-desc-list">
                            <li>Google TalkBack is tailored for Android-based systems. Nevertheless, it lacks explicit design for literacy needs, necessitating third-party assistance to obtain literacy materials.</li>
                            <li>Vocalizer is a text-to-speech application for visually impaired Android users, relies significantly on touch and mandates that all components be visibly displayed on the screen.</li>
                            <li>NVDA is intended for personal computer operation but is limited by the reliance on manual input, requiring a complete personal computer setup.</li>
                        </ul>
                    </div>

                    <div>
                        <h4>Features</h4>
                    </div>
                    <div>
                        <ul class="work-description-desc-list">
                            <li><b>Penjelajah</b> serves as a feature that utilizes a filtered search engine to discover literacy materials.</li>
                            <li><b>Riwayat</b> is a feature designed to store the history of reading activities.</li>
                            <li><b>Koleksi</b> is a feature offering a growing collection of free audiobooks.</li>
                            <li><b>Bantuan</b> functions as an on-page user guide.</li>
                            <li><b>Widget</b> provides the quickest route to launch the Literaku application.</li>
                        </ul>
                    </div>

                    <div>
                        <h4>Result</h4>
                    </div>
                    <div>
                        <img class="work-detail-img" src="img/logo/literaku.png" alt="logo-literaku" style="width:100px">
                        <p>During an initial observation at SLB-A YAPENTRA, there was enthusiasm and support for developing an application aimed at enhancing voice-based literacy for the blind through commands and listening. These findings open up possibilities for creating voice-based reading resources tailored for the visually impaired.
                        </p>
                        <p>The solutions to enhance blind literacy are seamlessly integrated into the Literaku application, an Android-based platform designed to empower individuals with visual impairments to enhance their literacy independently. The Literaku application's contribution lies in optimizing Indonesian voice commands by understanding the contextual meaning of nearby words, supported by Natural Language Processing. This facilitates visually impaired individuals in locating readings and executing various application activities through voice commands and listening.
                        </p>
                        <p>The Literaku application proves to be an effective and efficient solution for addressing the literacy needs of the blind. The high level of user satisfaction, reaching 89.60%, indicates that users find the Literaku application highly satisfactory. Moreover, the application aligns with and promotes educational opportunities for all, in line with Sustainable Development Goal 4 (SDG4).
                        </p>
